    A = np.polyval(rng.standard_normal(deg + 1), S)
    B = np.polyval(rng.standard_normal(deg + 1), S)
    C = np.polyval(rng.standard_normal(deg + 1), S)
    D = np.polyval(rng.standard_normal(deg + 1), S)
    F1 = A + C * w1**2 + w1 * (B + D * w1**2)
    F2 = A + C * w2**2 + w2 * (B + D * w2**2)
    ok = np.all(F1 > 0) and np.all(F2 < 0)
    if ok:
        found_local_metric = True
        break
    # record where opposite-sign first fails, among candidates that start opposite at s=0
