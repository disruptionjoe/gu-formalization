# The metric symbol carries sign(lambda) = lambda/omega = lambda/sqrt(k^2+m^2).  A local (finite
# order / compact kernel) operator has an ENTIRE symbol (Paley-Wiener).  1/sqrt(k^2+m^2) has a branch
