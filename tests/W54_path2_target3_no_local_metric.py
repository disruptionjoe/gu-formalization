# The unique ENTIRE (even, polynomial) grading through the SHELLS (om1:+1, om2:-1), even in lambda:
#   C_even(lambda) = g0 + g2*lambda^2,  g2 = 2/(om1^2-om2^2) = -2/Delta,  g0 = 1 - g2*om1^2.
g2 = 2 / (om1**2 - om2**2)
g0 = 1 - g2 * om1**2
Cev_om1 = sp.simplify(g0 + g2 * om1**2)   # = +1
Cev_om2 = sp.simplify(g0 + g2 * om2**2)   # = -1
