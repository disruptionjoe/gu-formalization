ratio = (F1c + F2c) / (F1c - F2c)
lim = sp.limit(ratio.subs({m1: 1, m2: 2, A0: 1, B0: 1, C0: 1, D0: 1}), s, sp.oo)
check("D1''b  symbolic: |F1+F2|/|F1-F2| -> oo as k->oo for a constant-symbol metric "
      "=> the two shells acquire the SAME sign at large k (opposite signs impossible for a local "
      "symbol); the sign flip requires the non-entire sqrt",
      lim in (sp.oo, -sp.oo),
      f"lim (F1+F2)/(F1-F2) = {lim} (magnitude diverges => same sign at large k)")

