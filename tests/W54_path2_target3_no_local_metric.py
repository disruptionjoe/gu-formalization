# ------------------------------------------------------------------------------------------------
# Symbolic setup.  s = k^2 >= 0.  Two mass shells: m1 (healthy), m2 (ghost).  om_i = sqrt(s+m_i^2).
# Krein one-particle signature (the load-bearing physical input, stated explicitly):
#   the 4th-order propagator 1/[(k^2+m1^2)(k^2+m2^2)] partial-fractions with OPPOSITE residues
#   -> the two masses carry opposite KG norm sign (this IS the ghost).  Within each mass, positive-
#   and negative-frequency modes carry opposite KG sign.  On states (om1+, om1-, om2+, om2-):
#       eps = (+1, -1, -1, +1).
# A positive metric eta must convert every physical (positive-frequency, and its CPT partner) state
# to positive norm; equivalently the grading C = eta^{-1} J has eigenvalue eps_i on state i.
# ------------------------------------------------------------------------------------------------
s, m1, m2, d1, d2, lam = sp.symbols("s m1 m2 d1 d2 lambda", positive=True)
om1 = sp.sqrt(s + m1**2)
om2 = sp.sqrt(s + m2**2)
Delta = m2**2 - m1**2  # om2^2 - om1^2, a nonzero constant (distinct masses)

# ================================================================================================
# D1 -- COMMUTANT CHARACTERIZATION + SIGN OBSTRUCTION  (the crux)
# ================================================================================================
log("\n[D1] Commutant characterization + sign obstruction")

# General positive metric <=> grading C with eigenvalues (d1,-d1,-d2,d2) on (om1,-om1,om2,-om2),
# with d1,d2>0 the positive commutant weights (particle/antiparticle equal by CPT/reality).
# This sign pattern is ODD under lambda->-lambda, so the unique degree<=3 interpolant is
#   C(lambda) = alpha*lambda + beta*lambda^3.
# Solve the 2x2 system  alpha*om_i + beta*om_i^3 = value_i.
alpha, beta = sp.symbols("alpha beta")
sol = sp.solve(
    [sp.Eq(alpha * om1 + beta * om1**3, d1),
     sp.Eq(alpha * om2 + beta * om2**3, -d2)],
    [alpha, beta], dict=True,
)[0]
alpha_s = sp.simplify(sol[alpha])
beta_s = sp.simplify(sol[beta])
log(f"     alpha(coeff of lambda ) = {alpha_s}")
log(f"     beta (coeff of lambda^3) = {beta_s}")

# Closed forms claimed in the docstring:
alpha_claim = (d1 * om2**2 / om1 + d2 * om1**2 / om2) / Delta
beta_claim = -(d1 / om1 + d2 / om2) / Delta
check("D1a  odd interpolant coefficients match the closed forms "
      "alpha=[d1 om2^2/om1 + d2 om1^2/om2]/Delta, beta=-[d1/om1 + d2/om2]/Delta",
      sp.simplify(alpha_s - alpha_claim) == 0 and sp.simplify(beta_s - beta_claim) == 0,
      "closed-form commutant family verified")

# beta is a STRICTLY-SIGNED positive combination of 1/om1, 1/om2 for all d1,d2>0: never zero,
# always carries both branch cuts.  Show beta*(-Delta) = d1/om1 + d2/om2 > 0 and is non-rational.
beta_core = sp.simplify(-beta_s * Delta)  # = d1/om1 + d2/om2
check("D1b  beta = -(d1/om1 + d2/om2)/Delta is a positive combo of 1/om1,1/om2 for ALL d1,d2>0 "
      "=> the lambda^3 coefficient NEVER vanishes and always carries 1/sqrt(k^2+m_i^2)",
      sp.simplify(beta_core - (d1 / om1 + d2 / om2)) == 0,
      "no positive commutant weight can cancel the odd (non-entire) part")

# Prove non-entirety concretely: beta_core has a branch point at s = -m1^2 (radius m1 in the
# expansion in s about 0).  Its Taylor series in s has finite radius = m1^2 (nearest singularity),
# i.e. the metric symbol is NOT entire.  Ratio test on the binomial series of 1/om1 (m1=1):
# c_j = binom(-1/2,j) with |c_j/c_{j+1}| = 1 + 1/(2j+1) -> 1 (radius = m1^2 = 1), approached slowly
# from above, so average high-order ratios.
