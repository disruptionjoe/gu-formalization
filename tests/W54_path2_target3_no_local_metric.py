check("D1'a  the even grading C_even=g0+g2*lambda^2 is ENTIRE (g2=-2/Delta const, g0 poly in s) "
      "and hits the shell values (+1 on om1, -1 on om2)",
      g2_is_entire and Cev_om1 == 1 and Cev_om2 == -1,
      "entire even grading exists at the SHELL level")

