#     r - y r' = y^2 e^y/(e^y-1)^2 ;  P = y + r = y e^y/(e^y-1). ---
def _exp_pieces(y: np.ndarray, s: float = 1.0):
    ey = np.exp(y)
    denom = ey - 1.0
    num = s * (y * y) * ey / (denom * denom)   # r - y r'
    P = y + s * y / denom                       # y + r
    return num, P


def phi_exp(p: int, n: int, s: float = 1.0) -> float:
    num, P = _exp_pieces(_Y, s)
    integrand = (_Y ** (n - 1)) * num / (P ** p)
    return _simpson(integrand) / math.gamma(n)


