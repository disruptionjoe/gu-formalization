def _simpson(fvals: np.ndarray) -> float:
    s = fvals[0] + fvals[-1] + 4.0 * np.sum(fvals[1:-1:2]) + 2.0 * np.sum(fvals[2:-2:2])
    return float(s * _DY / 3.0)


